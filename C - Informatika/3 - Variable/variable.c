#include <stdio.h>

int main() {
    // Declare an integer variable
    int myInt = 10;

    // Declare a float variable
    float myFloat = 3.14;

    // Declare a character variable
    char myChar = 'A';

    // Print the variables
    printf("Integer: %d\n", myInt);
    printf("Float: %.2f\n", myFloat);
    printf("Character: %c\n", myChar);

    return 0;
}